package com.example.inventaristoko.Model.Kategori;

public class Kategori {
    private String id;
    private String idKategori;
    private String namaKategori;
    private String tanggalTambahKategori;
    private String tanggalUbahKategori;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getIdKategori() {
        return idKategori;
    }

    public void setIdKategori(String idKategori) {
        this.idKategori = idKategori;
    }

    public String getNamaKategori() {
        return namaKategori;
    }

    public void setNamaKategori(String namaKategori) {
        this.namaKategori = namaKategori;
    }

    public String getTanggalTambahKategori() {
        return tanggalTambahKategori;
    }

    public void setTanggalTambahKategori(String tanggalTambahKategori) {
        this.tanggalTambahKategori = tanggalTambahKategori;
    }

    public String getTanggalUbahKategori() {
        return tanggalUbahKategori;
    }

    public void setTanggalUbahKategori(String tanggalUbahKategori) {
        this.tanggalUbahKategori = tanggalUbahKategori;
    }
}
